    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String identificador = jTextField1.getText().trim();
        String senha = new String(jPasswordField1.getPassword());

        // Validação básica
        if (identificador.isEmpty() || senha.isEmpty()) {
            mostrarMensagemErro("Preencha todos os campos!");
            return;
        }

        // Autenticação via SistemaController
        String tipoUsuario = sistemaController.autenticarUsuario(identificador, senha);
        
        switch (tipoUsuario) {
            case "admin":
                abrirTelaAdmin(identificador);
                break;
            case "gestor":
                abrirTelaGestor(identificador);
                break;
            case "funcionario":
                abrirTelaFuncionario(identificador);
                break;
            case "cliente":
                abrirTelaCliente(identificador);
                break;
            default:
                mostrarCredenciaisTeste();
                break;
        }
    }//GEN-LAST:event_jButton2ActionPerformed

