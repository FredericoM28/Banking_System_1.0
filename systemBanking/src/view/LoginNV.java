    private void abrirTelaAdmin(String identificador) {
        String nomeUsuario = sistemaController.getNomeUsuario(identificador, "admin");
        mostrarMensagemSucesso("Login de Administrador bem-sucedido!", "Bem-vindo, " + nomeUsuario);
        
        TelaAdmn admin = new TelaAdmn();
        admin.setVisible(true);
        this.dispose();
    }

    private void abrirTelaGestor(String identificador) {
        String nomeUsuario = sistemaController.getNomeUsuario(identificador, "gestor");
        mostrarMensagemSucesso("Login de Gestor bem-sucedido!", "Bem-vindo, " + nomeUsuario);
        
        TelaGestor gestor = new TelaGestor();
        
        JFrame frameGestor = new JFrame("Banco Nexus - Painel do Gestor");
        frameGestor.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
        frameGestor.setSize(1000, 600);
        frameGestor.setLocationRelativeTo(null);
        frameGestor.add(gestor);
        frameGestor.setVisible(true);
        
        this.dispose();
    }

    private void abrirTelaFuncionario(String identificador) {
        String nomeUsuario = sistemaController.getNomeUsuario(identificador, "funcionario");
        mostrarMensagemSucesso("Login de Funcionário bem-sucedido!", "Bem-vindo, " + nomeUsuario);
        
        // Abre o PainelFuncAtendimento
        PainelFuncAtendimento painelFunc = new PainelFuncAtendimento(sistemaController, nomeUsuario, "Atendimento");
        painelFunc.setVisible(true);
        
        this.dispose();
    }

    private void abrirTelaCliente(String identificador) {
        Cliente cliente = sistemaController.getClienteAutenticado(identificador);
        if (cliente != null) {
            mostrarMensagemSucesso("Login de Cliente bem-sucedido!", 
                "Bem-vindo, " + cliente.getNomeCli());
            
            // Passe o cliente autenticado para a TelaCliente
            TelaCliente telaCliente = new TelaCliente(cliente);
            
            JFrame frameCliente = new JFrame("Banco Nexus - Área do Cliente - " + cliente.getNomeCli());
            frameCliente.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
            frameCliente.setSize(1000, 600);
            frameCliente.setLocationRelativeTo(null);
            frameCliente.add(telaCliente);
            frameCliente.setVisible(true);
            
            this.dispose();
        } else {
            mostrarMensagemErro("Erro ao carregar dados do cliente!");
        }
    }

    private void mostrarMensagemSucesso(String titulo, String mensagem) {
        JOptionPane.showMessageDialog(this, mensagem, titulo, JOptionPane.INFORMATION_MESSAGE);
    }

    private void mostrarMensagemErro(String mensagem) {
        JOptionPane.showMessageDialog(this, mensagem, "Erro", JOptionPane.ERROR_MESSAGE);
    }

    private void mostrarCredenciaisTeste() {
        // Mostra clientes disponíveis para teste
        StringBuilder clientesDisponiveis = new StringBuilder();
        java.util.List<Cliente> clientes = sistemaController.listarClientes();
        
        if (!clientes.isEmpty()) {
            clientesDisponiveis.append("<b>Clientes disponíveis:</b><br>");
            for (Cliente cliente : clientes) {
                clientesDisponiveis.append("• Email: <code>").append(cliente.getEmailCli())
                    .append("</code> ou ID: <code>").append(cliente.getIdCliente())
                    .append("</code><br>");
            }
            clientesDisponiveis.append("• Senha padrão: <code>1234</code><br><br>");
        }
        
        String mensagem = 
            "<html><body style='width: 350px;'>" +
            "<h3>Credenciais de Teste</h3>" +
            "<b>Administrador:</b><br>" +
            "• Usuário: <code>admin</code><br>" +
            "• Senha: <code>admin123</code><br><br>" +
            
            "<b>Gestor:</b><br>" +
            "• Usuário: <code>gestor</code><br>" +
            "• Senha: <code>gestor123</code><br><br>" +
            
            "<b>Funcionário:</b><br>" +
            "• Usuário: <code>funcionario</code><br>" +
            "• Senha: <code>funcionario123</code><br><br>" +
            
            clientesDisponiveis.toString() +
            
            "<i>Dica: Use o email ou ID do cliente com senha 1234</i>" +
            "</body></html>";
        
        JOptionPane.showMessageDialog(this, mensagem, "Credenciais Inválidas", JOptionPane.ERROR_MESSAGE);
    }

